package Items;

public class Jacket {
    private int waist, sleeveLength, length, chest, shoulders;
    private String model;

    public Jacket(int waist, int sleeveLength, int length, int chest, int shoulders, String model) {
        this.waist = waist;
        this.sleeveLength = sleeveLength;
        this.length = length;
        this.chest = chest;
        this.shoulders = shoulders;
        this.model = model;
    }
}
